// ─── SEARCH HELPERS ───────────────────────────────────────────────────────────

const normalize = (str) => (str || '').toLowerCase().trim().replace(/\s+/g, ' ');

// Returns library entries whose canonical name or any alias contains the search term.
// Case-insensitive and whitespace-normalized. Returns [] for queries under 2 chars.
export const findGameInLibrary = (searchTerm) => {
  const q = normalize(searchTerm);
  if (q.length < 2) return [];
  return GAME_LIBRARY.filter(game => {
    if (normalize(game.canonicalName).includes(q)) return true;
    return game.aliases.some(alias => normalize(alias).includes(q));
  });
};
